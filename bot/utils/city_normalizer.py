import re
from typing import Dict


# Словарь алиасов городов
# Ключ - нормализованный вариант написания, значение - каноническое название
CITY_ALIASES: Dict[str, str] = {
    # Москва
    "мск": "москва",
    "msk": "москва",
    "moscow": "москва",
    "moskow": "москва",
    
    # Санкт-Петербург
    "спб": "санкт-петербург",
    "питер": "санкт-петербург",
    "spb": "санкт-петербург",
    "saint petersburg": "санкт-петербург",
    "санкт петербург": "санкт-петербург",
    "петербург": "санкт-петербург",
    
    # Екатеринбург
    "екб": "екатеринбург",
    "ekb": "екатеринбург",
    "yekaterinburg": "екатеринбург",
}


def normalize_city(city: str) -> str:
    """
    Нормализует название города к единому формату.
    
    Шаги:
    1. Удаляет пробелы в начале и конце
    2. Приводит к нижнему регистру
    3. Удаляет префиксы типа "г.", "гор.", "city"
    4. Удаляет точки, запятые и лишние символы
    5. Заменяет множественные пробелы на один
    6. Проверяет словарь алиасов
    7. Если алиас не найден, возвращает очищенное значение
    
    Args:
        city: Исходное название города
        
    Returns:
        Нормализованное название города
    """
    if not city:
        return ""
    
    # 1. Удаляем пробелы в начале и конце
    normalized = city.strip()
    
    # 2. Приводим к нижнему регистру
    normalized = normalized.lower()
    
    # 3. Удаляем префиксы типа "г.", "гор.", "city", "town"
    prefixes_to_remove = [r"^г\.\s*", r"^гор\.\s*", r"^city\s*", r"^town\s*"]
    for prefix in prefixes_to_remove:
        normalized = re.sub(prefix, "", normalized)
    
    # 4. Удаляем точки, запятые и лишние символы (оставляем буквы, цифры, пробелы, дефисы)
    normalized = re.sub(r"[^\w\s\-]", "", normalized)
    
    # 5. Заменяем множественные пробелы на один
    normalized = re.sub(r"\s+", " ", normalized)
    
    # 6. Удаляем пробелы в начале и конце после очистки
    normalized = normalized.strip()
    
    # 7. Проверяем словарь алиасов
    if normalized in CITY_ALIASES:
        return CITY_ALIASES[normalized]
    
    # 8. Fallback: возвращаем очищенное значение
    return normalized
